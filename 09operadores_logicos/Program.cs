// Variáveis
double projeto1 = 7;
double projeto2 = 5;

// Operador E (todas afirmações devem ser true)
Console.WriteLine(projeto1 >= 7 && projeto2 >= 7);

// Operador OU (uma afirmação deve ser true)
Console.WriteLine(projeto1 >= 7 || projeto2 >= 7);

// Operador de NEGAÇÃO
Console.WriteLine(!true);               